#include <iostream>

using namespace std;

int main(int argc, char *argv[])
{
int num, one, ten, hundred;

cout << "Enter an integer between 0 and 999: ";
cin >> num;
cout << endl;

hundred = num/100;
ten = (num % 100)/10;
one = ((num % 100) % 10);

cout << "1's digit is " << one << endl;
cout << "10's digit is " << ten << endl;
cout << "100's digit is " << hundred << endl;

return 0;

}
